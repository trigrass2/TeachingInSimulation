public class BrokenCase {
	// 故障名称
	private String name;
	// 对应机型
	private String model;
	// 故障路径
	private String trafficPath;

	public String getName() {
		return name;
	}

	public String getModel() {
		return model;
	}

	public String getTrafficPath() {
		return trafficPath;
	}
}
